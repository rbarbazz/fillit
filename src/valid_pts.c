int			valid_dots(char *buffer)
{
	int		i;
	int		nbpts;

	i = 0;
	nbpts = 12;
	while (i < 20)
	{
		if (buffer[i] == '.')
			nbpts--;
		i++;
	}
	if (nbpts != 0)
		return (1);
	return (0);
}
